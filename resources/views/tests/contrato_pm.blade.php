<p style='text-align:center'>D E C L A R A C I O N E S</p> 
<br>
I.- YO, LICENCIADO MANUEL HUMBERTO GALLARDO INZUNZA, EN REPRESENTACION DE “GALLBO” DECLARO LO SIGUIENTE:

a).- Que mi representada es una sociedad civil, constituida conforme a las leyes del país, mediante escritura pública número 2,449, de fecha 10 de agosto de 1998, pasada ante la fe del Lic. José Guadalupe Hoyos Hernández, Notario Público No 15 de Ciudad Obregón, Sonora, e inscrita en el Registro Público de Comercio de ese Distrito Judicial, en la sección “registro de personas morales”, bajo el número 193,701, libro uno, volumen 20, en fecha 14 de agosto de 1998; y que obtuvo previamente el permiso correspondiente de la Secretaría de Relaciones Exteriores, para operar como empresa de asesoría jurídica y corporativa de riesgos, teniendo como actividad exclusiva la asesoría en la celebración de contratos de seguros, así como reclamaciones de todo tipo de indemnizaciones derivadas de éstos.

b).- Que mi representada cuenta con la experiencia, recursos económicos, humanos y materiales necesarios para proporcionar asesoría jurídica y técnica, necesaria para presentar reclamaciones de indemnización, extra judicial o judicialmente ante “LA(S) ASEGURADORA(S)”, por los daños que sufrieron los bienes asegurados por “LA(S) POLIZA(S)” que más adelante se señalará(n), a consecuencia de “EL SINIESTRO”.

II.- YO, _______________________, EN MI CARÁCTER DE REPRESENTANTE LEGAL DE LA EMPRESA “_____________________”, DECLARO LO SIGUIENTE:

a).- Que mi representada, como ____________ es titular de los derechos derivados de “LA(S) POLIZA(S), expedida(s)  por “LA(S) ASEGURADORA(S)”.

b).- Que mi representada se encuentra al corriente en los pagos de las primas devengadas por las coberturas amparadas por “LA(S) POLIZA(S)” citada(s) en este instrumento.

c).- Que en la fecha a que se refiere este instrumento, los bienes cubiertos por “LA(S) POLIZA(S)” antes citada(s), sufrieron daños a consecuencia de “EL SINIESTRO” que más adelante se precisa.

d).- Que es interés de mi representada, presentar formal reclamación de indemnización a “LA(S) ASEGURADORA(S)”, para obtener los pagos indemnizatorios establecidos en “LA(S) POLIZA(S)”.


III.- AMBAS PARTES; DECLARAMOS LO SIGUIENTE:

a).- Que para efectos de este contrato se entenderá lo siguiente:

“LA(S) POLIZA(S)”.- La póliza número _______________, expedida por la empresa aseguradora _______________.

“EL SINIESTRO”.- Daños materiales a ___ ____________ asegurado(s) por “LA(S) POLIZA(S)” a que se refiere este instrumento, ocurrido en _________________ en ______________; a consecuencia del _________________________. 

“LA(S) ASEGURADORA(S)”.- __________________. 

b).- Que se reconocen expresamente la personalidad con que comparecen a la firma de este documento y que es su voluntad celebrar el presente contrato, el cual sujetan  al tenor de las siguientes:

C L Á U S U L A S

PRIMERA: OBLIGACIONES DE “GALLBO”. 

a).- Prestar a “EL CONTRATANTE” los servicios de asesoría jurídica y técnica especializada, a partir de la firma del presente contrato, consistentes en la revisión e integración de los documentos e informes necesarios para demostrar las circunstancias de realización y consecuencias de “EL SINIESTRO”. 

b).- Elaborar la(s) carta(s) de formal reclamación de pago, dirigidas a “LA(S) ASEGURADORA(S)”, por virtud de la cual se reclame el pago de las prestaciones que le corresponden a “EL CONTRATANTE”, al haberse actualizado el evento previsto en “LA(S) POLIZA(S)” a que se refiere este contrato.

c).- Celebrar, previa autorización e instrucción expresa de “EL CONTRATANTE” los convenios de ajuste que correspondan, derivados de la integración de la documentación solicitada por “LA(S) ASEGURADORA(S)”, para tener por acreditadas las circunstancias de realización de “EL SINIESTRO” y sus consecuencias.

d).- Representar a “EL CONTRATANTE” ante la Comisión Nacional para la Protección y Defensa de los Usuarios de Servicios Financieros, en caso de que “LA(S) ASEGURADORA(S)” se niegue(n), injustificadamente a cubrir total o parcialmente las prestaciones que se derivan exclusivamente de “LA(S) POLIZA(S)” y de “EL SINIESTRO” objeto del presente contrato.

e).- Representar a “EL CONTRATANTE” ante los Tribunales Judiciales competentes, ante el eventual caso que “LA(S) ASEGURADORA(S)” se niegue(n) a cubrir las prestaciones que se derivan exclusivamente de “LA(S) POLIZA(S)” y de “EL SINIESTRO” reclamado, previo agotamiento de la etapa conciliatoria ante CONDUSEF.

f).- Presentar, dentro de las 48 horas después de la firma de este contrato, calendario y programa de actividades para el desarrollo de la asesoría objeto del presente instrumento.


Así mismo, “GALLBO” se obliga, dentro del plazo a que se refiere el párrafo anterior, a proporcionar la SOLICITUD FORMAL DE DOCUMENTOS E INFORMES que formarán parte del expediente de reclamación. 

SEGUNDA.- OBLIGACIONES DE “EL CONTRATANTE” A PARTIR DE LA FIRMA DEL PRESENTE INSTRUMENTO.
 
a).- Se obliga a proporcionar a “GALLBO”, dentro de los plazos y términos establecidos de común acuerdo en el calendario y programa de actividades, todos y cada uno de los documentos e informes necesarios, que le sean solicitados, para demostrar las circunstancias de realización de “EL SINIESTRO” y sus consecuencias.

b).- Se obliga a proporcionar todas las facilidades, así como los elementos técnicos, materiales y humanos necesarios para el conocimiento y evaluación por parte de “GALLBO” de los daños y valores que le corresponden a los bienes asegurados y aquellos que fueron afectados por “EL SINIESTRO”.
 
Asimismo, se obliga a proporcionar todos los elementos técnicos y materiales para integrar el(los) expediente(s) de reclamación que deberán ser entregados a “LA(S) ASEGURADORA(S)”

c).- Se obliga a ejecutar todas las recomendaciones emitidas por “GALLBO”, que sean necesarias y que estén dentro del marco legal permitido, para demostrar las circunstancias de realización del siniestro y sus consecuencias.

d).- Se obliga a no rendir declaraciones a cualquier corporación o empresa aseguradora, por conducto de sus representantes o empleados; y no deberá entregar documentación alguna relacionada con sus bienes y riesgos cubiertos, así como aquellos relacionados con las causas y consecuencias del siniestro, sin consultarlo previamente con el personal de “GALLBO”.

e).- Se obliga a no realizar en forma verbal o escrita, transacciones, arreglos o convenios con “LA(S) ASEGURADORA(S)”, sin recabar y avisar previamente  a “GALLBO”.

f).- Deberá otorgar poder Especial para pleitos y cobranzas, limitado al ejercicio de todos los derechos que correspondan al asegurado por “LA(S) POLIZA(S)” señalada(s)  en este instrumento, a favor de las siguientes personas: LICS. MANUEL HUMBERTO GALLARDO INZUNZA, LUZ ELIA MARTINEZ JABALERA, ARLYN DANUBY VIDAL MACIAS, MELANIE ABIGAY GUARDADO MARTINEZ, RODOLFO ACOSTA BAEZ, SERGIO MIGUEL GUZMAN OROZCO Y LUIS ELIAS MEZA GIL, para que lo ejerzan de manera separada, con facultades expresas para: presentar reclamaciones de indemnización; celebrar convenios de ajuste y determinación de pérdidas, exhibir y recibir documentos  e informes y todas las facultades necesarias que se requieran para obtener las indemnizaciones que conforme a “LA(S) POLIZA(S)” citada(s) y a las disposiciones legales aplicables le correspondan a “EL CONTRATANTE”. 

TERCERA: HONORARIOS. 

Convienen las partes que los honorarios que devengará “GALLBO”, por los servicios de asesoría objeto del presente contrato, serán en base a las siguientes reglas:

I.- PORCENTAJE: A partir de la firma del presente contrato, “EL CONTRATANTE” pagará a “GALLBO” una cantidad equivalente al __% más I.V.A. (_____), de cualquier importe efectivamente anticipado o pagado totalmente por la aseguradora, o de quien acredite su interés asegurable o de cualquier tercero afectado, que reciba cualquier indemnización con cargo a la(s) Póliza(s), incluyendo el valor que el asegurado o cualquier otra persona cubra a la(s) aseguradora(s) por concepto de compra de salvamento."
 
II.- ANTICIPO: La cantidad de __________, más IVA (Monto en letra), a la firma del presente contrato.

III.- RECLAMACIÓN JUDICIAL.- Para el eventual caso de que la reclamación se plantee ante CONDUSEF o en forma de demanda ante los tribunales judiciales competentes, los honorarios a favor de "GALLBO" serán previamente negociados.  

IV.- FECHA Y LUGAR DE PAGO.- “EL CONTRATANTE” se obliga a cubrir a “GALLBO” en el domicilio ubicado en: Calle Frida Kahlo # 2411, entre Blvd. Universitarios y Diego Rivera, Col. Rincón Alamedas, en Culiacán, Sinaloa; mediante una sola exhibición las cantidades que por concepto de honorarios resultan de aplicar el porcentaje pactado, más el correspondiente Impuesto al Valor Agregado, dentro de las 48 horas siguientes a aquel en que reciba cualquier indemnización, emitida por “LA(S) ASEGURADORA(S)”.

“GALLBO” se obliga a entregar el recibo original de honorarios profesionales, con todos los requisitos fiscales vigentes a “EL CONTRATANTE” contra la entrega del pago correspondiente.

Los pagos se podrán realizar por medio de transferencia electrónica a la cuenta y clabe Interbancaria del banco en la cual “GALLBO” es beneficiario.

Convienen ambas partes que por los honorarios que a favor de “GALLBO” se generen por la prestación de los servicios a que se refiere este instrumento, podrán expedirse recibos a “EL CONTRATANTE” por cualquiera de las siguientes personas morales que integran a GRUPO GALLBO: “ASESORÍA JURÍDICA Y CORPORATIVA DE RIESGOS S.C.”; “RECLAMACIÓN ESTRATÉGICA ESPECIALIZADA S.C.”; “GALLBO S.C.”; “INGENIERÍA RECONSTRUCTIVA S.A. DE C.V.”; “FUNDACIÓN GALLBO A.C.” ó “DESARROLLO DE PROYECTOS ESTRATÉGICOS S.C.”
 

CUARTA: GASTOS.

“EL CONTRATANTE” Cubrirá el 100% de todos y cada uno de los gastos que se generen por concepto de hospedaje, alimentación,  transportación local y transportación aérea para _______ personas, con originen o  destino a la ciudad de _____________, o hacia cualquier parte de la Republica, con motivo de la atención a “EL SINIESTRO” y de los servicios a que se refiere este instrumento, INCLUYENDO aquellos gastos necesarios para la demostración de sus causas y consecuencias (Determinación de daño, cuantificación de daños, Reclamación de daños y Ajuste de daños).
 
Los gastos necesarios que se requieran podrán ser cubiertos directamente por “EL CONTRATANTE” a los prestadores de servicios; anticipados ó reembolsados a “GALLBO” dentro de las 48 horas siguientes que se hagan llegar las facturas correspondientes a dicho concepto.

En caso de reclamación presentada ante los tribunales judiciales competentes, todos y cada uno de los gastos son a cargo de “EL CONTRATANTE” y deberá reembolsar por este concepto a “GALLBO” las cantidades erogadas para la presentación de la misma previa autorización por escrito por parte de “EL CONTRATANTE”.


QUINTA: NATURALEZA DE ESTE CONTRATO.

Convienen expresamente las partes, que este contrato es de naturaleza civil y que para su vigencia, interpretación y cumplimiento forzoso, se someten expresamente a la competencia de los tribunales judiciales del Estado de Culiacán, Sinaloa.


SEXTA: DOMICILIOS.

Para todos los efectos legales contenidos en este instrumento, las partes señalan los siguientes domicilios:

“GALLBO”: Frida Kahlo #2411, entre Universitarios y Diego Rivera, Col. Rincon Alamedas, C.P. 80019, en Culiacán, Sinaloa.

“EL CONTRATANTE”: _________________________.




SEPTIMA: COMPETENCIA.

Convienen las partes que los tribunales competentes para resolver cualquier controversia que se suscite con motivo de la celebración y cumplimiento de este contrato, son los ubicados en Culiacán, Sinaloa, renunciando desde este momento a cualquier fuero que en el presente o futuro les pudiere corresponder, en razón de sus domicilios y se someten expresamente a la competencia de los Juzgados y Tribunales competentes de la Ciudad de Culiacán, Sinaloa.


OCTAVA: CONFIDENCIALIDAD.

“GALLBO” se obliga y compromete expresamente a mantener en absoluto secreto y confidencialidad profesional toda la información y documentación proporcionada por “EL CONTRATANTE” para el cumplimiento del objetivo de este contrato.


En caso de incumplimiento de las obligaciones establecidas a “GALLBO” en el presente Contrato, este será el único responsable, asumiendo las consecuencias derivadas de dicho incumplimiento, pagando a “EL CONTRATANTE” los daños y perjuicios que le ocasione.


“EL CONTRATANTE” se obliga y compromete a no divulgar las estrategias de reclamación implementadas por “GALLBO” para la presentación formal de la reclamación a “LA ASEGURADORA” (S); y no facilitará a terceros los formatos escritos o electrónicos proporcionados por “GALLBO”.


NOVEA: PUBLICIDAD DEL CONTRATO.

“EL CONTRATANTE” y “GALLBO” podrán autorizar por escrito mutuamente a publicar en cualquier medio e idioma, la existencia de este contrato y los resultados de los servicios prestados.

“EL CONTRATANTE” autoriza a “GALLBO” a reproducir y/o utilizar en medios impresos, electrónicos o de cualquier naturaleza, su logotipo y/o marca, única y exclusivamente como referencia comercial y para fines ilustrativos, siempre y cuando el logotipo y marca se encuentren impresos en formatos institucionales de “GALLBO” y estén acompañados de logotipos y marcas de terceros. 
 

“GALLBO” se obliga a no realizar modificación de ninguna naturaleza al logotipo de “EL CONTRATANTE”.



DECIMA: GARANTIAS DE AUTENTICIDAD EN DOCUMENTOS E INFORMES.

“EL CONTRATANTE” se obliga expresamente a no omitir hechos ni circunstancias o alterar de forma alguna los documentos, informes o declaraciones que rinda a “GALLBO” ó a cualquier autoridad que por razón de su competencia deba conocer de “EL SINIESTRO”; y que serán necesarios para demostrar las circunstancias de realización del siniestro y sus consecuencias.

“EL CONTRATANTE” garantiza a “GALLBO” la autenticidad y veracidad tanto en forma como contenido, de los documentos, informes o declaraciones que se proporcionen para la integración del expediente de reclamación que será entregado a “LA(S) ASEGURADORA(S)”.


DECIMO PRIMERA: CONDENA EN GASTOS Y COSTAS.
 
Convienen ambas partes que “EL CONTRATANTE”, es el único y exclusivo obligado a cubrir la eventual condena en GASTOS y COSTAS dictada por cualquier Tribunal Judicial.

Convienen también ambas partes, que corresponderán a “EL CONTRATANTE” los importes recuperados judicialmente por concepto de GASTOS, y que le corresponderán a “GALLBO”, las COSTAS que fueren demandadas y cubiertas por “LA ASEGURADORA”.


