<p style='text-align:justify; font-weight:bold;'>CONTRATO DE PRESTACIÓN DE SERVICIOS PROFESIONALES ESPECIALIZADOS, QUE CELEBRAN POR UNA PRIMERA PARTE “ASESORIA JURIDICA Y CORPORATIVA DE RIESGOS S.C.”, REPRESENTADA POR EL LICENCIADO MANUEL HUMBERTO GALLARDO INZUNZA, A QUIEN EN LO SUCESIVO SE LE DENOMINARÁ “GALLBO”; POR UNA SEGUNDA PARTE  “_______________”, REPRESENTADA POR SU REPRESENTANTE LEGAL C. _________________, A QUIEN EN LO SUCESIVO SE LE DENOMINARÁ “EL CONTRATANTE”; EL CUAL SUJETAN AL TENOR DE LAS DECLARACIONES Y CLÁUSULAS SIGUIENTES:</p>
